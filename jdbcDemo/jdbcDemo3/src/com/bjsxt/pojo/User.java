package com.bjsxt.pojo;

public class User {
	private int 用户ID;
	private String 昵称;
	private String 注册时间;
	private int 积分;
	private String 性别;
	public int get用户ID() {
		return 用户ID;
	}
	public void set用户ID(int 用户id) {
		用户ID = 用户id;
	}
	public String get昵称() {
		return 昵称;
	}
	public void set昵称(String 昵称) {
		this.昵称 = 昵称;
	}
	public String get注册时间() {
		return 注册时间;
	}
	public void set注册时间(String 注册时间) {
		this.注册时间 = 注册时间;
	}
	public int get积分() {
		return 积分;
	}
	public void set积分(int 积分) {
		this.积分 = 积分;
	}
	public String get性别() {
		return 性别;
	}
	public void set性别(String 性别) {
		this.性别 = 性别;
	}
	
	
	
}
